let lista = []

//.length é uma das propriedades do array a qual retorna o tamanho inteiro do array
console.log(lista.length)

//adiciona um elemento na lista no final do array
lista.push(5)
lista.push(8)

console.log(lista)

// adicionar  um elemento no inicio do array 
lista.unshift(0)


console.log(lista)


//remove o primeiro elemento do array. Como é o primeiro elemento não há necessidade de informar o paramentro
lista.shift()

console.log(lista)


//remove o ultimo elemento
//ultimo elemento, não há necessidade de informar paramento
lista.pop()

console.log(lista)